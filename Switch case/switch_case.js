//switch case use 
var digit = prompt("Enter a digit : ");
switch (digit) {
    case "0":
        {
            document.write("Your Digit Zero");
            break;
        }
        case "1":
        {
            document.write("Your Digit One");
            break;
        }
        case "2":
        {
            document.write("Your Digit Two");
            break;
        }
        case "3":
        {
            document.write("Your Digit Three");
            break;
        }
        case "4":
        {
            document.write("Your Digit Four");
            break;
        }
        case "5":
        {
            document.write("Your Digit Five");
            break;
        }
        case "6":
        {
            document.write("Your Digit Six");
            break;
        }
        case "7":
        {
            document.write("Your Digit Seven");
            break;
        }
        case "8":
        {
            document.write("Your Digit Eight");
            break;
        }
        case "9":
        {
            document.write("Your Digit Nine");
            break;
        }


    default:
        {
            document.write("Not a Digit ");
            break;

        }
        
}