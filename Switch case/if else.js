//digit spelling 
//0-zero,1-one,9-nine , 10-- not a digit
var digit = prompt("Enter a Digit : ");
if (digit==0)
 {
   document.write("Press Zero"); 
}
else if (digit==1)
{
    document.write("Press One ");  
}
else if (digit==2)
{
    document.write("Press Two ");  
}
else if (digit==3)
{
    document.write("Press Three ");  
}
else if (digit==4)
{
    document.write("Press Four ");  
}
else if (digit==5)
{
    document.write("Press Five ");  
}
else if (digit==6)
{
    document.write("Press Six ");  
}
else if (digit==7)
{
    document.write("Press Seven ");  
}
else if (digit==8)
{
    document.write("Press Eight ");  
}
else if (digit==9)
{
    document.write("Press Nine ");  
}
else
{
    document.write("Not a Digit");
}